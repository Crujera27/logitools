const isAdmin = (req, res, next) => {
    if (req.isAuthenticated()) {
        if(req.user.isAdmin=="1"){
          return next()
        }else{
          return res.redirect("/dashboard")
        }
    } else {
      req.session.returnTo = req.originalUrl;
      res.redirect('/login');
    }
  };
    export default isAdmin;